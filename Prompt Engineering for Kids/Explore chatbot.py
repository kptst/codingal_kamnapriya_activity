# Activity – Explore Chatbots

# Objective:
# Compare how different chatbots respond to the same prompt and observe differences in style, detail, and creativity.

# Steps:

# Open any two or more chatbots (ChatGPT, Gemini, Claude, Copilot).

# Type the same prompt in each chatbot. Example:
# “Tell me 3 fun facts about the Moon.”

# Copy their responses into a comparison table.

# Try another prompt (story, riddle, or coding).

# Refine one prompt by adding more details.

# Compare the new responses with the original ones.

# Write 4–5 sentences about what you observed.

# What to Look For:

# What is similar?

# What is different?

# Which answer is clearer?

# Which one do you like most and why?

# Key Learning:
# Clear prompts = better and more detailed answers.